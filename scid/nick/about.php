                <span id="title_text"><i>What is SCID?</i></span>
            
             <p />         
                           
The Sanctuary Characterization and Image Display (SCID) is a user-friendly, map-based interface for viewing imagery and summary data collected in the Monterey Bay National Marine Sanctuary (MBNMS) using a variety of underwater sampling tools, including towed camera sleds, remotely operated vehicles (ROVs) and human-occupied submersibles. SCID was designed, and continues to be supported, by scientists at the <a href="http://sep.csumb.edu/ifame">Institute for Applied Marine Ecology (IfAME)</a> at California State University Monterey Bay. 
       
            <p />  
            <span id="title_text"><i>How to Use SCID</i></span>       
            <p />          
            SCID contains imagery from seven major Study Areas in the MBNMS. You can select a Study Area from the pull-down menu (on the left side of the main page). Next, you can select viewable transects by different data collection instruments (camera sled, ROV, and submersible). Finally, the Media  pull-down menu lets you choose the type of media you would like to view: video or still photographic imagery, or both.  
            <p /> 
            <span id="body_image"><img src="images/scid_layout/sled_screen_details.jpg" /></span>
            Once you have selected an area, zooming into that area you will find transect lines along which imagery has been collected. Red tear drops represent video clips. Yellow tear drops represent still photographs or frame grabs from video.
              <p /> 
              Important operational information is recorded on the video image, and can be seen on the photos and videos in SCID. For camera sled imagery, the date the transect was conducted can be seen in the upper right-hand corner of the image, along with the altitude of the sled in meters (Beware- the altitude is not always accurate). The time of the transect (in Greenwich Mean Time or GMT) can be found in the upper left-hand corner, along with the depth of the sled in meters.
               <p /> 
               Most organism identifications are done in the field and then verified later using the recorded video. If you can identify something we have not – please let us know!
      
         <p />   
           <span id="title_text"><i>Rationale for SCID</i></span>       
         <p />
            Effective public education in marine science, particularly in circumstances where pressing environmental issues are concerned, depends on the timely and transparent dissemination of information from the scientific community to the public. Where excessively complex, non-intuitive scientific graphics can limit knowledge exchange and indeed undermine educational efforts, well-designed, accessible graphics can facilitate learning and increase the public’s enthusiasm for science in general and marine science in particular. 
            <p />
            However, the public rarely receives scientific information directly from scientists or in a timely manner, and the resource managers who depend on the support of an informed public frequently lack important information on many locations, resources, and ecological processes in the areas they manage, even where a great deal of science and monitoring has been conducted, like the MBNMS. 
            <p />
            The MBNMS was established in 1992, with the help of extensive public support, for the purpose of resource protection, research, education, and public use of the environment contained within its boundaries. The largest of the 13 National Marine Sanctuaries, the MBNMS encompasses a shoreline length of 444 kilometers and 13,784 square kilometers of ocean. SCID is dedicated to the informing the public and supporting management of the MBNMS.
      
