<span id="body_image"><img src="images/scid_layout/fulmar_web.jpg" alt="Fulmar" width="204" height="150"/></span>
            The imagery and data presented in SCID are collected using a variety of research platforms, including the MBNMS’ research vessel, FULMAR, as well as other research and fishing vessels. Further, there are a variety of vehicles used to collect imagery, including towed camera sleds and remotely operated vehicles and human-occupied submersibles such as the Delta submersible. 
            <p/>
<span id="body_image"><img src="images/scid_layout/sled_schemamtic_web.jpg" alt="Camera sled" width="204" height="150"/></span>
During a research cruise, imagery is collected continuously as a selected vehicle is towed or flown along a transect line. Collection of data in real-time is undertaken using a programmable keyboard that allows scientists to press a key each time a particular organism or habitat feature is observed in the video. The keyboard notes the organism, the time, and the precise location of the observation. 
<p/>
<span id="body_image"><img src="images/scid_layout/delta_web_scid.jpg" alt="Delta" width="204" height="150"/></span>
Post-processing of video and still photographic imagery is done by scientists at their home institutions (such as the IfAME at CSU Monterey Bay). Here identifications are verified, representative clips from all study areas are created, and videos and photos are selected and posted to SCID.  Click here to see more on some of the on-going projects.
